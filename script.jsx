function switchTab(type) {
  // Remove existing active class
  document.querySelectorAll('.tab').forEach(tab => tab.classList.remove('active'));

  // Hide both concern sections
  document.getElementById('skin-concerns').classList.add('hidden');
  document.getElementById('hair-concerns').classList.add('hidden');

  // Clear product list
  document.getElementById('product-list').innerHTML = '';

  // Show relevant concern section
  if (type === 'skin') {
    document.getElementById('skin-concerns').classList.remove('hidden');
    document.querySelector('.tab:nth-child(1)').classList.add('active');
  } else if (type === 'hair') {
    document.getElementById('hair-concerns').classList.remove('hidden');
    document.querySelector('.tab:nth-child(2)').classList.add('active');
  }
}

function showProducts(category, concern) {
  const productList = document.getElementById('product-list');
  productList.innerHTML = '';

  const concernData = category === 'skin' ? skinConcerns : hairConcerns;
  const products = concernData[concern];

  products.forEach(product => {
    const card = document.createElement('div');
    card.className = 'product-card';
    card.innerHTML = `
      <img src="${product.image}" alt="${product.name}">
      <h3>${product.name}</h3>
      <p><strong>Rating:</strong> ${product.rating}</p>
      <p>${product.benefits}</p>
      <a href="${product.link}" target="_blank">View on Amazon</a>
    `;
    productList.appendChild(card);
  });
}